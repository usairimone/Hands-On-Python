# Create three variables:
# a = 1
# b = 2
# c = 3
# Swap them so that:
# a = 3
# b = 1
# c = 2
# Print the values before and after swapping.


# initialize three int varaibles
a = 1
b = 2
c = 3

# swap the values
a, b, c = c, a, b

# prints values after swapping
print(f"The value of a after swapping is {a}")
print(f"The value of b after swapping is {b}")
print(f"The value of c after swapping is {c}")