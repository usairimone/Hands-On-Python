# Create a variable named celsius with a temperature value.
# Convert it to Fahrenheit using the formula:
# F = (C * 9/5) + 32
# Print both Celsius and Fahrenheit values.
# Use type() to display the data types.


# initialize celsius variable with an integer value
celsius = 42
fahrenheit = (celsius * 9/5) + 32
# print all both values with their types
print(f"Celsius: {celsius}°C")
print(f"Fahrenheit: {fahrenheit}°F")
print(f"Type of celsius: {type(celsius)}")
print(f"Type of fahrenheit: {type(fahrenheit)}")